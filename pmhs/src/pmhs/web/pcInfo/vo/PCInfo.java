package pmhs.web.pcInfo.vo;

public class PCInfo {
	private int num;
	private String unit;
	private String department;
	private int lectureRoom;
	private int isDeclare;
	private int isReservation;
	
	public int getNum() {
		return num;
	}
	public void setNum(int num) {
		this.num = num;
	}
	public String getUnit() {
		return unit;
	}
	public void setUnit(String unit) {
		this.unit = unit;
	}
	public String getDepartment() {
		return department;
	}
	public void setDepartment(String department) {
		this.department = department;
	}
	public int getLectureRoom() {
		return lectureRoom;
	}
	public void setLectureRoom(int lectureRoom) {
		this.lectureRoom = lectureRoom;
	}
	public int getIsDeclare() {
		return isDeclare;
	}
	public void setIsDeclare(int isDeclare) {
		this.isDeclare = isDeclare;
	}
	public int getIsReservation() {
		return isReservation;
	}
	public void setIsReservation(int isReservation) {
		this.isReservation = isReservation;
	}
	
}
